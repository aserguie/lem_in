#include "../inc/libft.h"

void	ft_putendl(char const *str)
{
	int		i;
	char	*s;

	i = 0;
	s = (char *)str;
	while (str[i] != '\0')
	{
		ft_putchar(s[i]);
		i++;
	}
	write(1, "\n", 1);
}
