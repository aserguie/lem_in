#include "../inc/libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*src;

	src = b;
	i = 0;
	while (i < len)
	{
		src[i] = c;
		i++;
	}
	return (b);
}
