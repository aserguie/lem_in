#include "../inc/lem_in.h"

void	ft_free_string_array(char **tab)
{
	int	i;

	i = 0;
	if (tab)
	{
		while (tab[i])
		{
			ft_strdel(&tab[i]);
			i++;
		}
		ft_memdel((void**)&tab);
	}
}
